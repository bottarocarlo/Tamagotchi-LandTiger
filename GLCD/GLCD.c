/******************************************************************************
* Function Name  : PutChar
* Description    : 繍Lcd徳貧販吭了崔塋紹燦忖憲
* Input          : - Xpos: 邦峠恫炎 
*                  - Ypos: 換岷恫炎  
*				   - ASCI: 塋承鍔峽
*				   - charColor: 忖憲冲弼   
*				   - bkColor: 嘘尚冲弼 
* Output         : None
* Return         : None
* Attention		 : None
*******************************************************************************/
void PutChar( uint16_t Xpos, uint16_t Ypos, uint8_t ASCI, uint16_t charColor, uint16_t bkColor )
{
	uint16_t i, j;
    uint8_t buffer[16], tmp_char;
    GetASCIICode(buffer,ASCI);  /* 函忖庁方象 */
    for( i=0; i<16; i++ )
    {
        tmp_char = buffer[i];
        for( j=0; j<8; j++ )
        {
            if( ((tmp_char >> (7 - j)) & 0x01) == 0x01 )
            {
                LCD_SetPoint( Xpos + j, Ypos + i, charColor );  /* 忖憲冲弼 */
            }
            else
            {
                LCD_SetPoint( Xpos + j, Ypos + i, bkColor );  /* 嘘尚冲弼 */
            }
        }
    }
}

/******************************************************************************
* Function Name  : GUI_Text
* Description    : 壓峺協恙炎塋衝峽堪
* Input          : - Xpos: 佩恙炎
*                  - Ypos: 双恙炎 
*				   - str: 忖憲堪
*				   - charColor: 忖憲冲弼   
*				   - bkColor: 嘘尚冲弼 
* Output         : None
* Return         : None
* Attention		 : None
*******************************************************************************/
void GUI_Text(uint16_t Xpos, uint16_t Ypos, uint8_t *str,uint16_t Color, uint16_t bkColor)
{
    uint8_t TempChar;
    do
    {
        TempChar = *str++;  
        PutChar( Xpos, Ypos, TempChar, Color, bkColor );    
        if( Xpos < MAX_X - 8 )
        {
            Xpos += 8;
        } 
        else if ( Ypos < MAX_Y - 16 )
        {
            Xpos = 0;
            Ypos += 16;
        }   
        else
        {
            Xpos = 0;
            Ypos = 0;
        }    
    }
    while ( *str != 0 );
}

